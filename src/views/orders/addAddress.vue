<template>
  <div class="addAddress">
     <Header :isLeft="true" :title="title"/>
     <div class="viewbody">
         <div class="content">
              <FormBlock type="text" placeholder="姓名" label="联系人" 
              v-model="addressInfo.name"
              :tags="sexes" 
              :sex="addressInfo.sex" 
              @checkSex="checkSex"/>
              <FormBlock type="number" placeholder="手机号码" label="电话" v-model="addressInfo.phone"/>
              <FormBlock  placeholder="小区／写字楼／学校等" 
              label="地址"  
              icon="angle-right" 
              textarea="textarea"
              v-model="addressInfo.address"
              @click="showAddress=true"
              />
              <FormBlock  placeholder="10号楼5层101" label="门牌号"  icon="edit" textarea="textarea" v-model="addressInfo.bottom"/>
              <div class="formblock">
                <div class="label-wrap">标签</div>
               <TabTag :tags="tags" :selectTag="addressInfo.tag" @checkTag="checkTag"/>
              </div>
         </div>
         <div class="form-button-wrap">
            <button @click="handleSave" class="form-button">确定</button>
        </div>
     </div>
     <AddressSearch :showAddress="showAddress" @close="close" :addressInfo="addressInfo"/>
  </div>
</template>

<script>
import Header from "../../components/Header.vue";
import FormBlock from "../../components/orders/FormBlock.vue";
import TabTag from "../../components/orders/TabTag.vue";
import AddressSearch from "../../components/orders/AddressSearch.vue";
import { Toast } from 'mint-ui';
export default {
    components:{
        Header,
        FormBlock,
        TabTag,
        AddressSearch
    },
    data(){
        return{
            title:"添加地址",
            tags:["家","学校","公司"],
            sexes:["女","男"],
            addressInfo: {},
            showAddress:false
        }
    },
    beforeRouteEnter(to,from,next){
        next(vm=>{
            vm.title=to.params.title;
            vm.addressInfo=to.params.addressInfo;
        })
    },
    methods:{
       checkTag(item) {
        // console.log(item);
        this.addressInfo.tag = item;
        },
        checkSex(item) {
        // console.log(item);
        this.addressInfo.sex = item;
        },
        handleSave(){
            if(!this.addressInfo.name){
                this.showMsg("请填写联系人");
                return
            }
             if(!this.addressInfo.phone){
                this.showMsg("请填写手机号码");
                return
            }
             if(!this.addressInfo.address){
                 this.showMsg("请填写地址");
                 return
            }
             if(!this.addressInfo.bottom){
                 this.showMsg("请填写门牌号");
                 return
            }
            //添加数据
            if(this.title=="编辑地址"){
                this.editAddress()
            }else{
                this.addAddress();
            }
        },
        showMsg(msg){
            Toast({
                    message: msg,
                    position: 'bottom',
                    duration: 2000
                });
        },
        addAddress(){
            this.$axios.post(`/api/user/add_address/${localStorage.ele_login}`,this.addressInfo)
            .then(res=>{
              this.$router.push('/myAddress');
              if(!this.$store.getters.userInfo){
                this.$store.dispatch("setUserInfo",this.addressInfo);
              }
            })
            .catch(err=>console.log(err))
        },
        editAddress(){
             this.$axios.post(`/api/user/edit_address/${localStorage.ele_login}/${this.addressInfo._id}`,this.addressInfo)
            .then(res=>{
              this.$router.push('/myAddress');
            })
            .catch(err=>console.log(err))
        },
        close(){
            this.showAddress=false;
        }
    }

}
</script>

